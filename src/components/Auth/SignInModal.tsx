"use client";

import React, { useState } from "react";
import { useAuth } from "@/context/AuthContext";
import { Icon } from "@iconify/react";
import Logo from "@/components/Layout/Header/Logo";

interface SignInModalProps {
    onClose: () => void;
    onSwitchToSignUp?: () => void;
}

const SignInModal: React.FC<SignInModalProps> = ({ onClose, onSwitchToSignUp }) => {
    const { signInWithEmail, signInWithGoogle } = useAuth();
    const [email, setEmail] = useState("");
    const [password, setPassword] = useState("");
    const [error, setError] = useState("");
    const [loading, setLoading] = useState(false);

    const handleSubmit = async (e: React.FormEvent) => {
        e.preventDefault();
        setError("");
        setLoading(true);
        try {
            await signInWithEmail(email, password);
            onClose();
        } catch (err: any) {
            console.error("Sign in error:", err);
            const code = err?.code || "";
            if (code === "auth/user-not-found" || code === "auth/wrong-password" || code === "auth/invalid-credential") {
                setError("Invalid email or password.");
            } else if (code === "auth/too-many-requests") {
                setError("Too many attempts. Please try again later.");
            } else {
                setError("Sign in failed. Please try again.");
            }
        } finally {
            setLoading(false);
        }
    };

    const handleGoogleSignIn = async () => {
        setError("");
        try {
            await signInWithGoogle();
            onClose();
        } catch (err: any) {
            console.error("Google sign in error:", err);
            setError("Google sign in failed. Please try again.");
        }
    };

    return (
        <div className="fixed inset-0 w-full h-full bg-black/60 backdrop-blur-sm flex items-center justify-center z-120 p-4 transition-all duration-300">
            <div className="relative mx-auto w-full max-w-md overflow-hidden rounded-3xl bg-white px-8 py-10 dark:bg-darklight shadow-2xl animate-modal-in">
                <button
                    onClick={onClose}
                    className="hover:bg-gray-100 dark:hover:bg-white/10 p-2 rounded-full absolute top-4 right-4 transition-colors"
                    aria-label="Close"
                >
                    <Icon icon="ic:round-close" className="text-2xl dark:text-white" />
                </button>

                <div className="mb-8 text-center">
                    <div className="flex justify-center mb-6">
                        <Logo />
                    </div>
                    <h3 className="text-2xl font-extrabold text-midnight_text dark:text-white tracking-tight">Welcome Back</h3>
                    <p className="text-grey dark:text-white/60 text-sm mt-2">Enter your details to access your account</p>
                </div>

                {error && (
                    <div className="mb-6 p-4 bg-red-50 dark:bg-red-900/20 border border-red-100 dark:border-red-800/50 rounded-2xl text-red-600 dark:text-red-400 text-sm text-center font-medium animate-shake">
                        {error}
                    </div>
                )}

                <form onSubmit={handleSubmit} className="space-y-4">
                    <div>
                        <label className="block text-xs font-bold text-midnight_text dark:text-white uppercase tracking-wider mb-2 ml-1">Email Address</label>
                        <input
                            type="email"
                            placeholder="name@company.com"
                            required
                            value={email}
                            onChange={(e) => setEmail(e.target.value)}
                            className="w-full rounded-2xl border border-gray-200 dark:border-dark_border bg-gray-50/50 dark:bg-white/5 px-4 py-4 text-sm text-midnight_text dark:text-white outline-none transition focus:border-accent focus:ring-4 focus:ring-accent/10 placeholder:text-gray-400"
                        />
                    </div>
                    <div>
                        <div className="flex items-center justify-between mb-2 ml-1">
                            <label className="text-xs font-bold text-midnight_text dark:text-white uppercase tracking-wider">Password</label>
                            <button type="button" className="text-xs font-bold text-accent hover:underline">Forgot?</button>
                        </div>
                        <input
                            type="password"
                            required
                            value={password}
                            placeholder="••••••••"
                            onChange={(e) => setPassword(e.target.value)}
                            className="w-full rounded-2xl border border-gray-200 dark:border-dark_border bg-gray-50/50 dark:bg-white/5 px-4 py-4 text-sm text-midnight_text dark:text-white outline-none transition focus:border-accent focus:ring-4 focus:ring-accent/10 placeholder:text-gray-400"
                        />
                    </div>
                    <button
                        type="submit"
                        disabled={loading}
                        className="flex w-full items-center justify-center rounded-2xl bg-accent px-5 py-4 text-sm font-bold text-white shadow-lg shadow-accent/25 transition-all duration-300 hover:bg-accent/90 hover:scale-[1.02] active:scale-[0.98] disabled:opacity-50 disabled:scale-100"
                    >
                        {loading ? (
                            <div className="w-5 h-5 border-2 border-white border-t-transparent rounded-full animate-spin" />
                        ) : (
                            "Sign In to Account"
                        )}
                    </button>
                </form>

                <div className="relative my-8">
                    <div className="absolute inset-0 flex items-center">
                        <div className="w-full border-t border-gray-100 dark:border-dark_border" />
                    </div>
                    <div className="relative flex justify-center text-xs font-bold uppercase tracking-widest">
                        <span className="bg-white dark:bg-darklight px-4 text-grey dark:text-white/40">or continue with</span>
                    </div>
                </div>

                <button
                    onClick={handleGoogleSignIn}
                    className="flex w-full items-center justify-center gap-3 rounded-2xl border border-gray-200 dark:border-dark_border p-4 text-sm font-bold text-midnight_text dark:text-white hover:bg-gray-50 dark:hover:bg-white/5 transition-all duration-200"
                >
                    <svg width="20" height="20" viewBox="0 0 24 24" fill="none">
                        <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4" />
                        <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
                        <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l3.66-2.84z" fill="#FBBC05" />
                        <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 12-4.53z" fill="#EB4335" />
                    </svg>
                    Google Account
                </button>

                <p className="text-grey dark:text-white/50 text-sm text-center mt-8 font-medium">
                    New to MentorPace?{" "}
                    <button
                        onClick={onSwitchToSignUp}
                        className="text-accent hover:underline font-bold"
                    >
                        Create account
                    </button>
                </p>
            </div>
        </div>
    );
};

export default SignInModal;
